import { lineString } from '../lib/geojson'
import type { BiblePersonJourneyDTO, BiblePersonJourneyStopDTO, GeoJsonPosition } from '../domain/types'

type StopSeed = Omit<BiblePersonJourneyStopDTO, 'id' | 'sequence'>

interface PersonSeed {
  id: string
  person: string
  personZh: string
  era: string
  startYear: number | null
  endYear: number | null
  role: string
  color: string
  description: string
  scriptureRange: string
  stops: StopSeed[]
}

function journey(seed: PersonSeed): BiblePersonJourneyDTO {
  const stops = seed.stops.map((stop, index) => ({
    ...stop,
    id: `${seed.id}-${index + 1}`,
    sequence: index + 1,
  }))
  return {
    ...seed,
    routeGeojson: lineString(stops.map((s) => [s.longitude, s.latitude] as GeoJsonPosition)),
    stops,
  }
}

